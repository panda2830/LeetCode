#include <iostream>
using namespace std;

/********************************************************************************
 * @brief &位与运算符，同时为1时为1其他为0
 * 
 * @return int              
 ********************************************************************************/
int bit1()
{
    //? 位与运算符(二元运算符)
    //? 同时为1时为1，其他时为0
    int a = 10; // bin 1010
    int b = 12; // bin 1100
    /*
        1010
       &
        1100
        1000    十进制8
    */
    return a&b;
}

/********************************************************************************
 * @brief |位或运算符两个位置都为0时才为0，其他为1
 * 
 * @return int              
 ********************************************************************************/
int bit2()
{
    //? 位或运算符(二元运算符)
    //? 同时为0时为0，其他时为1
    int a = 10; // bin 1010
    int b = 12; // bin 1100
    /*
        1010
       |
        1100
        1110    十进制14
    */
    return a|b;
}

/********************************************************************************
 * @brief ^异或运算符，同位时为0，或则为1
 * 
 * @return int              
 ********************************************************************************/
int bit3()
{
    //? 异或算符(二元运算符)
    //? 同位时为0，不同时为1
    int a = 10; // bin 1010
    int b = 12; // bin 1100
    /*
        1010
       ^
        1100
        0110    十进制6
    */
    return a^b;
}

/********************************************************************************
 * @brief ~按位取反，0变1，1变0
 * 
 * @return int              
 ********************************************************************************/
int bit4()
{
    //? 按位取反(一元运算符)
    //? 0变1，1变0
    int a = 10; // bin 1010
    /*
        1010
       ~
        0101   十进制-11
    */
    return ~a;
}

/********************************************************************************
 * @brief <<左移运算符
 * 
 * @return int              
 ********************************************************************************/
int bit5()
{
    //? 左移运算符(二元运算符)
    //? 同位时为0，不同时为1
    int a = 10; // bin 1010
    /*
        1010
    <<
        1010000    十进制80
    */
    return a<<3;
}

/********************************************************************************
 * @brief >>右称运算符
 * 
 * @return int              
 ********************************************************************************/
int bit6()
{
    //? 右称运算符(二元运算符)
    //? 同位时为0，不同时为1
    int a = 10; // bin 1010
    /*
        1010
    >>
        1    十进制8
    */
    return a>>3;
}

int main(int argc, char **argv)
{
    cout << bit6() << endl;
    return 0;
}